
public class Calculadora {
	public int suma(int num1, int num2) {
		return num1 + num2;
	}//suma
	
	public int divide(int num1, int num2) {
		return num1 / num2;
	}//divide
	
	public double getPI(){
		return 3.1415926;
	}//getPI
	
	public String getPIStr(){
		return String.valueOf(getPI());
	}//getPIStr
}//class Calculadora
